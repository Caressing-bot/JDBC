public class Constant {
    public static void main(String[] args) {
        //整数常量
        System.out.println(12);
        //小数常量
        System.out.println(1.2);
        //字符常量
        System.out.println('A');
        //字符串常量
        System.out.println("Hello World");
        //布尔常量
        System.out.println(false);
        System.out.println(true);

    }
}
